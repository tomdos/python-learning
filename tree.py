

class TreeNode:
    def __init__(self, value = None, left = None, right = None):
        self.value = value
        self.left = left
        self.right = right

    def setLeft(self, left):
        self.left = left
        
    def setRight(self, right):
        self.right = right
        
    def setValue(self, value):
        self.value = value


